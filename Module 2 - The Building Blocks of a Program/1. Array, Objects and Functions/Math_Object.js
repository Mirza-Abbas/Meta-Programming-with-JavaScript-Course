//Number Constants:
console.log("PI Constant: " + Math.PI);
console.log("Euler's Constant: " + Math.E);
console.log("Natural Log of 2: " + Math.LN2);

//Rounding Methods:
console.log(Math.ceil(4.2));    //Rounds Up
console.log(Math.floor(2.2));   //Rounds Down
console.log(Math.round(7.5));   //Rounds Up if number >= 5
console.log(Math.round(9.2));   //Rounds Down if number < 5

console.log(Math.trunc(6.7));   //Removes Decimal

//Arithmetic and Calculus Methods:
console.log("Calculates Power:" + Math.pow(2,3));
console.log("Calculates Square Root: " + Math.sqrt(16));
console.log("Calculates Cube Root: " + Math.cbrt(8));
console.log("Returns absolute value: " + Math.abs(-10));
console.log("Logarithmic Method: " + Math.log(2));
console.log("Logarithmic Method: " + Math.log2(2));
console.log("Logarithmic Method: " + Math.log10(2));
console.log("Returns Minimum Value: " + Math.min(2,8));
