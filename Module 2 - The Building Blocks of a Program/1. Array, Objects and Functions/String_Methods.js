
//.length method - returns length of string
console.log("Hello".length);

//.charAt method - returns individual character at a specific index
console.log("Hello".charAt(1));

//.concat method - joins two strings
console.log("Wo".concat("rl").concat("d"));

//.indexof method - returns index of first position that matches a character
console.log("Hello".indexOf("l"));

//.lastIndex method - returns index of last position that matches a character
console.log("Hello".lastIndexOf("l"));

//.split method - splits string into an array of sub-strings
console.log("ho-ho-ho".split("-"));

//.toUpperCase method - changes casing of strings to Uppercase
console.log("Hello".toUpperCase());

//.toLowerCase method - changes casing of strings to Lowercase
console.log("Hello".toLowerCase());